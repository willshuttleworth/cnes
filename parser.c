int num_bytes(unsigned char opcode) {
    for(int i = 0; i < NUM_OPCODES; i++) {
        if(table.opcodes[i] == opcode) {
            return table.bytes[i];
        }
    }
    return 0;
}
//return array of bytes specified by opcode
//  arr[0] = num_bytes
//  arr[1..num_bytes] = bytes
unsigned char *parse(unsigned char *instructions, int pos, int len) {
    if(pos >= len) {
        puts("tried to access instruction out of range");
        exit(0);
    }
    unsigned char opcode = instructions[pos];
    int bytes = num_bytes(opcode);
    if(bytes == 1) {
        unsigned char *bytes = malloc(2);
        bytes[0] = 1;
        bytes[1] = opcode;
        return bytes;
    }
    else if(bytes == 2) {
        unsigned char *bytes = malloc(3);
        bytes[0] = 2;
        bytes[1] = opcode;
        bytes[2] = instructions[pos + 1];
        return bytes;
    }
    else if(bytes == 3) {
        unsigned char *bytes = malloc(4);
        bytes[0] = 3;
        bytes[1] = opcode;
        bytes[2] = instructions[pos + 2];
        bytes[3] = instructions[pos + 1];
        return bytes;
    }
    return NULL;
}
