typedef struct PPU {
    unsigned char *chrom;
    unsigned char *vram;
    unsigned char *palette;
    unsigned char *oam;

    // pointer to next open spot in oam2
    int curr;
    
    // registers
    unsigned char ctrl;
    unsigned char mask;
    unsigned char status;
    unsigned char oamaddr;
    unsigned char oamdata;
    unsigned char scroll;
    unsigned char addr;
    unsigned char data;
    unsigned short v;
    unsigned short t;
    unsigned char w;
    unsigned char read_buffer;

    // rendering info
    int ppu_cycle;
    int *nmi;
    int scanline;
    int dot;

    //sdl 
    SDL_Texture *texture;
    SDL_Renderer *renderer;
    unsigned char *pixels;
} PPU;


